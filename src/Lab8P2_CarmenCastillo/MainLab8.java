    private void btn_ISMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_ISMouseClicked
        boolean hihi = true;
        boolean correct = true;
        AdminUsuarios aUs = new AdminUsuarios("./Usuarios.usr");
        aUs.cargarArchivo();
        for (Usuarios c : aUs.getListaUsuarios()) {
            if (c.getContra().equals(txtf_ContraIS.getText()) && c.getUser().equals(txtf_UsuarioIS.getText())) {
                int indice = aUs.getListaUsuarios().indexOf(c);
                correct = false;
                usuario = txtf_UsuarioIS.getText();
                contra = txtf_ContraIS.getText();
                progressBUsuario.setMaximum(indice);
                progressCContra.setMaximum(contra.length());
                llenarcomboCompraCons();
                listarTablaCompra();
                listarTablaGaraje();
                listarTablaVender();
                HilosInicio hIn = new HilosInicio(progressBUsuario, progressCContra, indice, contra.length(), this, vParaUsuario);
                hIn.start();
                hihi = hIn.isVive();

            }
        }

        txtf_ContraIS.setText("");
        txtf_UsuarioIS.setText("");

        if (correct == true) {
            JOptionPane.showMessageDialog(null, "Usuario y/o Contraseña incorrectos.");
            txtf_ContraIS.setText("");
            txtf_UsuarioIS.setText("");
        }

    }//GEN-LAST:event_btn_ISMouseClicked

