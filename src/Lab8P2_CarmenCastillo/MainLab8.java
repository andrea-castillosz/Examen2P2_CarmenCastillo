    private void jt_venderKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jt_venderKeyPressed
        Carro ref = null;
        if (evt.getKeyCode() == evt.VK_DELETE) {
            if (jt_vender.getSelectedRow() >= 0) {
                //int posic = jt_com.getSelectedRow();

                AdminUsuarios aUs = new AdminUsuarios("./Usuarios.usr");
                aUs.cargarArchivo();

                for (Usuarios us : aUs.getListaUsuarios()) {
                    if (us.getUser().equals(usuario)) {
                        for (Carro c : us.getListaCarros()) {
                            if (jt_vender.getSelectedRow() == us.getListaCarros().indexOf(c)) {
                                HiloBarraVenta hCom = new HiloBarraVenta(BarraVenta, c);
                                hCom.start();
                                ref = c;

                            }

                        }
                        if (ref == null) {
                            JOptionPane.showMessageDialog(null, "Hubo un error.");
                        } else {
                            us.getListaCarros().remove(ref);
                            us.setCuantBanco(us.getCuantBanco() + ref.getPrecio());
                            aUs.escribirArchivo();
                            listarTablaVender();
                        }

                    }

                }
            }
        }
    }//GEN-LAST:event_jt_venderKeyPressed

