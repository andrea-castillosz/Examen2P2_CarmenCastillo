    public static boolean validarContrasena(String contrasena) {

        String regex = "^[a-zA-Z0-9-_&$%@]*$";
        Pattern patron = Pattern.compile(regex);
        Matcher matcher = patron.matcher(contrasena);

        return matcher.matches();
    }

